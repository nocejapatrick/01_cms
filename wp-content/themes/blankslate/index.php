                            <div class="post-content">
                            This easy Pad Thai Noodles with Prawns and Tofu dish is simple to make you can skip eating out and make this at home.

HOW TO MAKE IT:
1. Start with soaking the dry noodles in room temperature water for 20 minutes while prepping the other ingredients. The dry noodles should be fully submerged in water. You want the noodles to be pliable and firm, but not completely expanded and soft. Drain and set the noodles aside.

2. Heat the oil in a wok (or a large pot) over medium-high heat. Stir fry the tofu and shallots until fragrant and beginning to color. Add the prawns and stir fry for 2 minutes, until just cooked. Add the rice noodles, tamarind concentrate (or white vinegar, if using), sugar, fish sauce and dried shrimp (if using), stirring to combine. Add 1 or 2 tablespoons of water a little at a time if the noodle mixture is sticky.

3. Push the noodle mixture to one side of the pan and add the lightly beaten eggs to the wok. Allow the eggs to cook for about 1 minute before mixing everything together. Stir in the Chinese chives and bean sprouts for a few seconds before removing the wok from the heat.

4. Serve the Pad Thai immediately and garnish with crushed peanuts and lime wedges.

INGREDIENTS:
4 tablespoons avocado oil (you can use olive oil, canola oil, or vegetable oil)
1 package (12 oz.) extra firm tofu, cute into 1/2 inch x 2 inches strips
4 shallots, thinly sliced
1 pound raw prawns, shelled and deveined
8 ounces dried flat rice noodl
4 tablespoons tamarind concentrate
4 tablespoons coconut sugar (also called palm sugar)
3 tablespoons fish sauce (we used Red Boat brand)
1 tablespoon dried shrimp (optional)
4 large eggs, lightly beaten
1 cup Chinese chives, cut in 2 inches (optional)
Large handful bean sprouts
4 tablespoons crushed roasted and unsalted peanuts
1 lime, cut into wedges
