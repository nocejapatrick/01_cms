                            <div class="post-content">
                            Savory authentic Thai Green Curry Chicken (gaeng keow wan gai) featuring chunks of tender chicken with eggplant, zucchini, and fresh herbs.

HOW TO MAKE IT:
1. Start by cutting all ingredients in advance.
2. Place the diced eggplant into a colander. Generously sprinkle a small handful of sea salt over the diced eggplant and gently toss. Leave for 10 minutes and rinse off.
3. Heat the wok or pan over high heat until hot. Add 2 tablespoons of oil and heat until it starts shimmering. Add the chicken and stir fry 3-4 minutes until just cooked through. Remove and set aside.
4. Add the remaining oil to the pan and stir-fry the zucchini and eggplant for 4-5 minutes until golden brown.
5. Add the ginger, garlic, and green onion and stir-fry for a minute until fragrant. Add the curry paste and stir-fry for another minute.
6. Add the chicken stock, coconut milk, and fish sauce. Bring to a boil then reduce heat to medium low and simmer for 10 minutes.
7. Add the chicken back to the pan and simmer another 3 minutes.
8. Gently stir in the lime juice and let it simmer another minute. Season with salt and white pepper to taste.
9. Serve over jasmine rice and top with the cilantro, Thai basil, and crushed peanuts.

INGREDIENTS:
3 tablespoons peanut or canola oil
1 pound boneless chicken breast, cut into 1/8 inch thick slices
2 medium zucchini, quartered and cut into bite sized pieces
1 large Chinese eggplant, quartered and cut into bite sized pieces
4 green onions, cut into 1/2 inch pieces
2 cloves of garlic, minced
2.5 inch fresh ginger, minced
2 tablespoons green curry paste
2 cups chicken stock
1.5 cups coconut milk
2 tablespoons fish sauce
1 lime, juiced
Jasmine rice, cooked
1/2 cup cilantro, lightly chopped
1/3 cup Thai basil, julienned
1 handful unsalted roasted peanuts, lightly crushed
